#import "NSString+BCLib.h"

@implementation NSString (BCLib)
- (CGSize)stringSizeWithFixedWidth:(CGFloat)width
                              font:(UIFont *)font
                              text:(NSString *)text {
    
    return [NSString stringSizeWithFixedValue:width isWidthFix:YES font:font text:text];
}

- (CGSize)stringSizeWithFixedHeight:(CGFloat)height
                               font:(UIFont *)font
                               text:(NSString *)text {
    return [NSString stringSizeWithFixedValue:height isWidthFix:NO font:font text:text];
}

+ (CGSize)stringSizeWithFixedValue:(CGFloat)fixValue
                        isWidthFix:(BOOL)isWidthFix
                              font:(UIFont *)font
                              text:(NSString *)text {
    
    CGFloat width = isWidthFix ? fixValue : 0;
    CGFloat height = isWidthFix ? 0 : fixValue;
    CGSize textSize;
    
    textSize = [text boundingRectWithSize:CGSizeMake(width, height)
                                  options:NSStringDrawingUsesLineFragmentOrigin | NSStringDrawingUsesFontLeading
                               attributes:@{NSFontAttributeName:font}
                                  context:nil].size;
    return textSize;
}

// !!!: 排序
- (NSString *)pinYin {
    NSInteger step = self.length;
    NSString *pinYinString = [NSString string];
    if (step > 0) {
        for (NSUInteger index = 0 ; index < step; index++) {
            unichar c = [self characterAtIndex:index];
            char firstLetter = firstLetterOfChinesCharacter(c);
            pinYinString = [pinYinString stringByAppendingFormat:@"%c",firstLetter];
        }
    }else{
        pinYinString = @"";
    }
    return pinYinString;
}
